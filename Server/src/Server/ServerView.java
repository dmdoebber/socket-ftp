    public void addLog(String log)
    {
        LocalDateTime ldt =  LocalDateTime.now();
        String date = "[" + ldt.getHour() + ":" + ldt.getMinute() + ":" + ldt.getSecond() + "]  ";
        logs.append(date + log + "\n");
    }
    
